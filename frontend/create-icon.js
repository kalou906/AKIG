// Create a simple PNG icon using Node.js Canvas
const fs = require('fs');
const path = require('path');

// For now, just create a placeholder base64 PNG (1x1 white pixel)
// This prevents the error while we work on proper icons
const minimalPNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk size
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0xC0, 0x00, 0x00, 0x00, 0xC0, // 192x192
  0x08, 0x02, 0x00, 0x00, 0x00, // bit depth, color type, etc
  0x8D, 0x7B, 0x92, 0xFD, // CRC
  0x00, 0x00, 0x00, 0x00, // IDAT chunk (empty for now)
  0x49, 0x44, 0x41, 0x54,
  0x78, 0x9C, 0x62, 0x00, 0x00, 0x00, 0x00, 0x01,
  0x00, 0x00, 0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4,
  0x00, 0x00, 0x00, 0x00, // IEND chunk size
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

const iconPath = path.join(__dirname, 'public', 'icons', 'icon-192.png');
fs.writeFileSync(iconPath, minimalPNG);
